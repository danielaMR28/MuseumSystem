    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sal = txtIsbn.getText();
        
        if(sal.isEmpty()){
            salaList = ls.getSalas();
        }else{
            Visita room = ls.getSala(sal);
            
            if(room == null){
                JOptionPane
                        .showMessageDialog(null, 
                                "Error: Sala no disponible" + sal);
               return;
            }
            
            salaList.clear();
            salaList.add(room);
        }
        
        setTableData();
    }//GEN-LAST:event_jButton1ActionPerformed

