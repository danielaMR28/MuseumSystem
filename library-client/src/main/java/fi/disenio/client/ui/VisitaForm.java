    private void setTableData(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        
        Object[] rowData = new Object[4];
        for(Visita book : salaList){
            rowData[0] = book.getSal();
            rowData[1] = book.getExp();
            rowData[2] = book.getTem();
            rowData[3] = book.getUbi();
            
            model.addRow(rowData);
        }
        
    }
    
    public void setTableModel(){
        List<String> columns = new ArrayList<>();
        columns.add("Sala");
        columns.add("Exposicion");
        columns.add("Temporalidad");
        columns.add("Ubicación");
        
        TableModel tm = new DefaultTableModel(null, columns.toArray());
        jTable1.setModel(tm);
    }
    

